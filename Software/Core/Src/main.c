/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
    int16_t        i,ticker;
     uint8_t        keycode;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC_Init();
  MX_I2C1_Init();
  MX_I2C2_Init();
  MX_TIM1_Init();
  MX_TIM3_Init();
  MX_USART2_UART_Init();
  MX_USB_PCD_Init();
  MX_IWDG_Init();
  /* USER CODE BEGIN 2 */
    POWER_SUPPLY_HOLD(1);  // hold the power supply on
    KEYPAD_RESET(1);          // hold keypad in reset

    HAL_TIM_Base_Start(&htim1);

     HAL_IWDG_Refresh(&hiwdg);  // kick WDT

     DisplayInitialize();
     HAL_IWDG_Refresh(&hiwdg);  // kick WDT

     KeyboardInitialize();
     DoInitializeCalculator();

     i=0;
     while(i<ADC_CHANNEL_COUNT){
         adcValues[i++]=0;
     }

          pwrButtonEnabled=0;

       ticker = 0;
#if 1
            HAL_UART_Transmit(&huart2,(uint8_t *)"BOOTED\n\r",8, 1000);
#endif

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
      HAL_IWDG_Refresh(&hiwdg);  // kick WDT
      HAL_PWR_EnterSLEEPMode( PWR_LOWPOWERREGULATOR_ON, PWR_SLEEPENTRY_WFI );      // sleep 1ms

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

      keycode = ReadKeypad(); // check for new key codes
      if(pwrButtonEnabled==0){
          if(keycode==KEY_PWR_ON_OFF){  // button released, we can enable the ON/OFF function
              pwrButtonEnabled=1;
          }
      } else {
          if(keycode==(KEYDOWN_EVENT|KEY_PWR_ON_OFF)){  // button pressed
              POWER_SUPPLY_HOLD(0);  // turn the power supply off
          }
      }

      if(ticker==0)
          StartADCs();

      HandleLEDBlinker(ticker);

      (void)PopUARTQueue();


      DoRunCalculator(keycode);

      ticker++;
       if(ticker == 1000)
          ticker =0;
  }
  /* USER CODE END 3 */
}

